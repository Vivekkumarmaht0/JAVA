public class Loops {
  public static void main(String[] args) {
        // while, for, do-while
        // while (condition) --> executes the block as long as
        // condition is true
        int i = 1;
        while (i <= 10) {
          System.out.print(i + " ");
          i++;
        }
        System.out.println();
        for (int j = 1; j <= 10; j++) {
          System.out.print(j + " ");
        }

        // arrays, strings and collections --> for-each loop

  }
}